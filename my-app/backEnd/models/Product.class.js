class Product {
  constructor(id, name, description, categoryId, price, pictureUrl, material, color, dimensions, status) {
    this.id = id;
    this.name = name;
    this.description = description;
    this.categoryId = categoryId;
    this.price = price;
    this.pictureUrl = pictureUrl;
    this.material = material;
    this.color = color;
    this.dimensions = dimensions;
    this.status = status;
  }
}
